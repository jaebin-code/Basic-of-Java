package chap_02;

public class _01_Operation {
    public static void main(String[] args) {
        System.out.println(4+2);
        System.out.println(4-2);
        System.out.println(4*2);
        System.out.println(4/2);
        System.out.println(2 + 2 * 2);
        int val=10;
        val++;
        System.out.println(val);
        System.out.println(++val);
        System.out.println(val++);
    }
}
